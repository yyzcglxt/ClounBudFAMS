package cn.pojo;

import java.util.Date;
/**
 * 员工表
 */
public class Staff {
	private String staffId;					/*工号*/
	private String staffName;				/*姓名*/
	private Integer staffSex;				/*性别*/
	private Date staffBirth;				/*出生日期*/
	private String staffMailbox;			/*邮箱地址*/
	private String staffHome;				/*籍贯*/
	private String staffAddress;			/*现住址*/
	private String staffNation;				/*民族*/
	private String staffTelephone;			/*联系电话*/
	private Integer staffPosiId;			/*职位ID*/
	public String getStaffId() {
		return staffId;
	}
	public void setStaffId(String staffId) {
		this.staffId = staffId;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public Integer getStaffSex() {
		return staffSex;
	}
	public void setStaffSex(Integer staffSex) {
		this.staffSex = staffSex;
	}
	public Date getStaffBirth() {
		return staffBirth;
	}
	public void setStaffBirth(Date staffBirth) {
		this.staffBirth = staffBirth;
	}
	public String getStaffMailbox() {
		return staffMailbox;
	}
	public void setStaffMailbox(String staffMailbox) {
		this.staffMailbox = staffMailbox;
	}
	public String getStaffHome() {
		return staffHome;
	}
	public void setStaffHome(String staffHome) {
		this.staffHome = staffHome;
	}
	public String getStaffAddress() {
		return staffAddress;
	}
	public void setStaffAddress(String staffAddress) {
		this.staffAddress = staffAddress;
	}
	public String getStaffNation() {
		return staffNation;
	}
	public void setStaffNation(String staffNation) {
		this.staffNation = staffNation;
	}
	public String getStaffTelephone() {
		return staffTelephone;
	}
	public void setStaffTelephone(String staffTelephone) {
		this.staffTelephone = staffTelephone;
	}
	public Integer getStaffPosiId() {
		return staffPosiId;
	}
	public void setStaffPosiId(Integer staffPosiId) {
		this.staffPosiId = staffPosiId;
	}
	

	
}
